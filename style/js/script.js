document.addEventListener('DOMContentLoaded', () => {
    setLang('pt');
    ModalApp.init();

    // Scroll Reveal (Animação ao rolar a página)
    const revealObserver = new IntersectionObserver(entries => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, { threshold: 0.12 });

    document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

    // Cursor Personalizado (Apenas Desktop)
    if (window.innerWidth > 768) {
        const dot = document.getElementById('cursor');
        const ring = document.getElementById('cursor-ring');
        if (dot && ring) {
            document.addEventListener('mousemove', e => {
                dot.style.left = e.clientX + 'px';
                dot.style.top = e.clientY + 'px';
                ring.style.left = e.clientX + 'px';
                ring.style.top = e.clientY + 'px';
            });
        }
    }
});

/* =================================================================
   ANIMAÇÃO DE METEOROS (FONDO CANVAS)
   ================================================================= */
const canvas = document.getElementById('bg-canvas');
const ctx = canvas.getContext('2d');

let meteors = [];

// Redimensiona o canvas para ocupar a tela inteira
function resizeCanvas() {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
}

window.addEventListener('resize', resizeCanvas);
resizeCanvas();

class Meteor {
    constructor() {
        this.reset();
    }

    reset() {
        this.x = Math.random() * canvas.width;
        this.y = Math.random() * canvas.height * -0.5; // Começa acima da tela
        this.length = Math.random() * 80 + 50;         // Comprimento do rastro
        this.speed = Math.random() * 10 + 5;           // Velocidade
        this.opacity = 1;
        this.width = Math.random() * 2 + 1;            // Espessura do meteoro
    }

    update() {
        this.x += this.speed;
        this.y += this.speed;
        this.opacity -= 0.01; // Efeito de fading

        if (this.opacity <= 0 || this.y > canvas.height || this.x > canvas.width) {
            this.reset();
        }
    }

    draw() {
        ctx.beginPath();
        // Cria um gradiente para o rastro do meteoro
        const gradient = ctx.createLinearGradient(
            this.x, this.y, 
            this.x - this.length, this.y - this.length
        );
        gradient.addColorStop(0, `rgba(255, 255, 255, ${this.opacity})`);
        gradient.addColorStop(1, 'rgba(255, 255, 255, 0)');

        ctx.strokeStyle = gradient;
        ctx.lineWidth = this.width;
        ctx.lineCap = 'round';
        ctx.moveTo(this.x, this.y);
        ctx.lineTo(this.x - this.length, this.y - this.length);
        ctx.stroke();
    }
}

// Inicializa 8 meteoros simultâneos
for (let i = 0; i < 15; i++) {
    meteors.push(new Meteor());
}

function animate() {
    // Limpa o canvas com um leve rastro (efeito de movimento)
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    meteors.forEach(m => {
        m.update();
        m.draw();
    });

    requestAnimationFrame(animate);
}

animate();